public class Tabuada {
    public static void main (String[] args) {
        int numMulti = 9;

    System.out.println("\n=================================");
    System.out.println("Tabuada de multiplicação 9");
    System.out.println("=================================\n");

    for (int i = 1; i <= 10; i++) {
        System.out.println( i + "x" + numMulti + "=" + (i*numMulti));
    }
    System.out.println("\n--------------------------------------------------\n");
    System.out.println("Até que peguei a ideia facil!\n");
    }
}